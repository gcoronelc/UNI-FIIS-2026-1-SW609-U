    V.IDVENTA,
    V.CLIENTE,
    V.IMPORTE       AS IMPORTE_VENTA,
    SUM(D.SUBTOTAL) AS SUMA_SUBTOTALES,
    SUM(P.IMPORTE)  AS SUMA_PAGOS
